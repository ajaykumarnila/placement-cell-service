--Student
CREATE TABLE STUDENT_INFO
(
	STUDENT_ID SERIAL PRIMARY KEY,
	ROLL_NUMBER VARCHAR(20) UNIQUE NOT NULL,
	FIRST_NAME VARCHAR(50),
	MIDDLE_NAME VARCHAR(50),
	LAST_NAME VARCHAR(50),
	DATE_OF_BIRTH TIMESTAMP,
	GENDER VARCHAR(5),
	MOBILE_NUMBER SMALLINT UNIQUE NOT NULL,
	EMAIL VARCHAR(100) UNIQUE NOT NULL,
	CURRENT_ADDRESS VARCHAR(1000),
	PERMENENET_ADDRESS VARCHAR(1000),
	COURSE VARCHAR(50),
	ADMISSION_YEAR VARCHAR(5),
	PASSING_YEAR VARCHAR(5),
	SEMESTER SMALLINT,
	DIVISION VARCHAR(5),
	COLLEGE_NAME VARCHAR(100),
	SSC_PASSING_YEAR VARCHAR(5),
	SSC_MARKS SMALLINT,
	HSC_PASSING_YEAR SMALLINT,
	HSC_MARKS SMALLINT,
	HIGHEST_QUALIFICATION_NAME VARCHAR(50),
	HIGHEST_QUALIFICATION_PASSING_YEAR VARCHAR(5),
	HIGHEST_QUALIFICATION_MARKS SMALLINT,
	OTHER_GRADUATION_NAME VARCHAR(50),
	OTHER_GRADUATION_PASSING_YEAR VARCHAR(5),
	OTHER_GRADUATION_MARKS SMALLINT,
	GAP VARCHAR(5),
	GAP_START_DURATION TIMESTAMP,
	GAP_END_DURATION TIMESTAMP,
	SEM_1_MARKS SMALLINT,
	SEM_2_MARKS SMALLINT,
	SEM_3_MARKS SMALLINT,
	SEM_4_MARKS SMALLINT,
	ACTIVE_BACKLOG SMALLINT,
	TOTAL_BACKLOGS SMALLINT,
	EXPERIENCE VARCHAR(5),
	EXPERIENCE_START_DATE TIMESTAMP,
	EXPERIENCE_END_DATE TIMESTAMP,
	COMPANY_NAME VARCHAR(100),
	POSITION VARCHAR(50),
	ROLE VARCHAR(50)
);


--Admin

CREATE TABLE ADMIN
(
	ADMIN_ID SERIAL PRIMARY KEY,
	TITLE VARCHAR(10),
	FIRST_NAME VARCHAR(50),
	MIDDLE_NAME VARCHAR(50),
	LAST_NAME VARCHAR(50),
	EMAIL VARCHAR(100) UNIQUE NOT NULL,
	MOBILE_NUMBER VARCHAR(50),
	COURSE VARCHAR(50),
	COLLEGE_NAME VARCHAR(100)
);

--Company
CREATE TABLE COMPANY_DETAILS
(
	COMPANY_ID SERIAL PRIMARY KEY,
	COMPANY_NAME VARCHAR(200),
	ORG_TYPE VARCHAR(100),
	SECTOR VARCHAR(100),
	COMPANY_WEBSITE VARCHAR(100),
	COMPANY_INFO VARCHAR(2000),
	JOB_LOCATION VARCHAR(50),
	ROLE VARCHAR(50)
);

--Drive_details
CREATE TABLE DRIVE_DETAILS
(
	DRIVE_ID SERIAL PRIMARY KEY,
	COMPANY_ID SMALLINT,
	DRIVE_DATE TIMESTAMP,
	DRIVE_LOCATION VARCHAR(200),
	OFFERED_CTC VARCHAR(50),
	SSC_MARKS SMALLINT,
	HSC_MARKS SMALLINT,
	GRADUATION_MARKS SMALLINT,
	ACTIVE_BACKLOG_STATUS VARCHAR(20),
	MAX_ACTIVE_BACKLOG_ALLOWED SMALLINT,
	HISTORICAL_BACKLOG_STATUS VARCHAR(20),
	MAX_HIST_BACKLOG_ALLOWED SMALLINT
);

--Drive_status
CREATE TABLE DRIVE_STATUS
(
	DRIVE_ID SMALLINT,
	COMPANY_ID SMALLINT,
	ELIGIBLE_STUDENT_ID SMALLINT,
	APPLICATION_STATUS VARCHAR(50)
);

--Placement_status
CREATE_TABLE PLACEMENT_STATUS
(
	DRIVE_ID SMALLINT,
	COMPANY_ID SMALLINT,
	APPLIED_STUDENT_ID SMALLINT,
	APPLICATION_STATUS VARCHAR(20),
	DATE_OF_PLACEMENT TIMESTAMP,
	OFFERED_CTC VARCHAR(20)
);

--Login_details
CREATE TABLE LOGIN_DETAILS
(
	LOGIN_ID SMALLINT,
	PASSWORD VARCHAR(20),
	IS_ADMIN VARCHAR(2),
	LAST_LOGIN_DATE TIMESTAMP,
	LAST_PASSWORD_RESET TIMESTAMP,
	PASSWORD_EXPIRATION_DATE TIMESTAMP,
	ACCOUNT_STATUS VARCHAR(50)
);